Map::Map()
{
    cobble = TextureManager::LoadTexture("../assets/cobble.png");
    sand = TextureManager::LoadTexture("../assets/sand1.png");
    lava = TextureManager::LoadTexture("../assets/lava.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    dest.x = dest.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
}

Map::~Map()
{
    //dtor
}

void Map::LoadMap(int arr[23][40])
{
    for (int row = 0; row < 23; row++)
    {
        for (int column = 0; column < 40; column++) MapTexture[row][column] = arr[row][column];
    }
}

void Map::DrawMap()
{
    int type = 0;

    for (int row = 0; row < 23; row++)
    {
        for (int column = 0; column < 40; column++)
            {
                type = MapTexture[row][column];

                dest.x = column * 32;
                dest.y = row * 32;

                switch(type)
                {
                    case 0:
                        {
                            TextureManager::Draw(lava, src, dest);
                            break;
                        }

                    case 1:
                        {
                            TextureManager::Draw(sand, src, dest);
                            break;
                        }

                    case 2:
                        {
                            TextureManager::Draw(cobble, src, dest);
                            break;
                        }

                    default:
                        {
                            break;
                        }
                }
            }
    }
}

int Map::getBlockUnder(int p_x, int p_y) {
    return MapTexture[p_x][p_y];
}
